//  To verify if the fields are empty    
    public boolean verifyFields(){
        String fullname = jFullname_TextField.getText();
        String username = jUsername_TextField.getText();
        String phone = jPhone_TextField.getText();
        String pass = String.valueOf(jPasswordField.getPassword());
        String conpass = String.valueOf(jConfirmPasswordField.getPassword());
        
        if (fullname.equals("") || username.equals("") || phone.equals("") || pass.equals("") || conpass.equals("")){
            JOptionPane.showMessageDialog(null,"One or more fields empty");
            return false;
        }
        else if (!pass.equals(conpass)) {
            JOptionPane.showMessageDialog(null, "Passwords do not match");
            return false;
        }
        
        else{return true;}
    }

//  To check if the username already exists in the database
    public boolean checkUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exists = false;
        
        String query = "SELECT * FROM users WHERE username = ?";
        
        try {
            st = MyConn.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if (rs.next()){
                username_exists = true;
                JOptionPane.showMessageDialog(null, "Username already exists");
            }
            } 
        
        catch (SQLException ex) {
            Logger.getLogger(Registration_form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        return username_exists;
        }
        
    
    
    
