    private void jSelectimage_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSelectimage_ButtonActionPerformed
        // TODO add your handling code here:
        String path = null;
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter extension = new FileNameExtensionFilter("Images*","jpg","jpeg","png");
        chooser.addChoosableFileFilter(extension);
        
        int filestate = chooser.showSaveDialog(null);
        
        if (filestate == JFileChooser.APPROVE_OPTION){
            File selectedImage = chooser.getSelectedFile();
            path = selectedImage.getAbsolutePath();
            jImagepath_Label.setText(path);
            
            jimage_path = path;
        }        
    }//GEN-LAST:event_jSelectimage_ButtonActionPerformed

    private void jRegister_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRegister_ButtonActionPerformed
        // TODO add your handling code here:
        String fnameString = jFullname_TextField.getText();
        String usernameString = jUsername_TextField.getText();
        String passString = String.valueOf(jPasswordField.getPassword());
        String confirmpassString = String.valueOf(jConfirmPasswordField.getPassword());
        String phone = jPhone_TextField.getText();
        String gender = "Male";
        
        if (jFemale_RadioButton.isSelected()){
            gender = "Female";
        }
        
        if (verifyFields()){
            if(!checkUsername(usernameString)){
                PreparedStatement ps;
                ResultSet rs;
                String resgister_query = "INSERT INTO users(full_name, username, password, phone, gender, picture) VALUES (?,?,?,?,?,?)";
                
                try {
                    ps = MyConn.getConnection().prepareCall(resgister_query);
                    ps.setString(1, fnameString);
                    ps.setString(2, usernameString);
                    ps.setString(3, passString);
                    ps.setString(4, phone);
                    ps.setString(5, gender);
                    
                    try{
                        if (jimage_path!=null){
                            InputStream image = new FileInputStream(new File(jimage_path));
                            ps.setBlob(6,image);
                        }
                        else{
                            ps.setNull(6, java.sql.Types.NULL);
                        }
                        
                        
                        
                        if (ps.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(null, "Your account has been created");
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Check your information");
                        }
                    }
                    catch (FileNotFoundException | SQLException ex) {
                        Logger.getLogger(Registration_form.class.getName()).log(Level.SEVERE,null, ex);
                        
                    }
                    
                    
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_jRegister_ButtonActionPerformed

